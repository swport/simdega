                    <div class="mx-1000 mx-auto pt-3">
                        <div class="description">
                            <p>
                                It was the farsightedness of a few well educated visionaries of Simdega that this College was established in the year 1960 [the same year Ranchi University was established in Ranchi] in the S. S. High School premises of Simdega. People of Simdega generously donated money to meet the necessary expenditure. Government officials also extended full support. Thus the college started taking proper shape. A plot of land was donated by Sri Jagdish Prasad of Tukupani where the college building was constructed and was shifted in the present location within a very short time. The foundation of the main college building was laid by the then Governor of erstwhile Bihar state Hon'ble Sri Anant Shayanam Ayangaar. The College was affiliated to Ranchi University, Ranchi. </p>
                                
                            <p>The financial burden on the poor natives for college education abruptly came to a happy end as the nearest college      at Ranchi was 170 Kms from this place. Dreams of the poor and needy were fulfilled. </p>
                            <p>Gradually, this private college of the people of Simdega achieved Grant-in- Aid status and in 1977 became a constituent unit of Ranchi University, Ranchi. Thereafter education up to Honours level started in almost all subjects. </p>
                              <p>Presently, this is the remotest college under Ranchi University which caters to the needs of people living in a very large area extending to the fringes of the state of Odissa and Chhattisgarh. </p>
                             <p>The hilly terrain of this area has no industry, no water resource. So, the agricultural products are negligible. Natives largely depend upon forest products. The existence of a degree college here is a great relief to them as they get a degree from here which is so essential in todayâ€™s world for a better living. In these fifty- eight years the college has made significant contribution to the society and continues to do so in every possible way. Compared to other places, considerable standard has also been maintained, so products of this college have gone far and wide and have been serving some of the highest echelons of the country. Nursing, armed forces and sports, particularly hockey, are some of the pockets where the students of this institution may be in abundance. Today a sizable number of the students come from economically weaker sections and are from ST, SC, BC categories with approx 80 % girls on our rolls. Thus this college has covered a very significant journey striving constantly to fulfill the dreams of especially, the tribal population of this locality. 
                            </p>
